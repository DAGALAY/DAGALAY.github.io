  /**
   * 构建知识库上下文文本（注入系统提示词）
   */
  buildContext() {
    let context = "\n\n【参考知识库 - 建筑施工安全规范】\n";
    this.data.categories.forEach(cat => {
      context += "\n■ " + cat.name + "\n";
      cat.items.forEach(item => {
        context += "  - " + item.title + ": " + item.content + "\n";
      });
    });
    context += "\n请在回答时参考以上知识库内容，确保回答符合国家建筑施工安全规范标准。\n";
    return context;
  },

  /**
   * 关键词检索
   */
  search(query) {
    const results = [];
    const keywords = query.toLowerCase().split(/\s+/);
    this.data.categories.forEach(cat => {
      cat.items.forEach(item => {
        let score = 0;
        const text = (item.title + " " + item.content + " " + cat.name).toLowerCase();
        keywords.forEach(kw => {
          if (kw.length > 1 && text.includes(kw)) score++;
        });
        if (score > 0) results.push({ category: cat.name, title: item.title, content: item.content, score });
      });
    });
    results.sort((a, b) => b.score - a.score);
    return results.slice(0, 10);
  }
